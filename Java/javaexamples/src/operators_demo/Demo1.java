package operators_demo;

public class Demo1 {
    public static void main(String[] args) {
        int x = 10;
        int y = 100;
        System.out.println(x++);//post
        System.out.println(x);
        System.out.println(++y);//pre
        System.out.println(y);


    }
    /*
    Operators -helps you perform some logical, relational and comparisions
    between the java variables.
    Arithmetic - + * / %
    Relational > < >= <= == !=
    Logical && || !
    Assignment =
        - Short Circuit += -= /= %= *=
    Unary ++ --
    Bitwise &(and) |(or) ^(nor) <<(bit shift left) >>(bit shift right)
    */
}
